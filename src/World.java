	public void setMap(int[][] map)
	{
		this.map = map;
		cameraXMax = -(map[0].length-cellCount)*cellSize;
		cameraYMax = -(map.length-cellCount)*cellSize;
	}
	
	public int[][] getMap()
	{
		return map;
	}
	
	public int getCellSize()
	{
		return cellSize;
	}
	
	public boolean isGameRunning ()
	{
		return gameRunning;
	}
	
	public void stopGame ()
	{
		gameRunning = false;
	}
	
	public ArrayList<IWorldObject> getObjects ()
	{
		return objects;
	}
	
	public void addObject(IWorldObject obj)
	{
		//add object and add it's image to the game
		objects.add(obj);
		pane.getChildren().add(obj.getImg());
	}
	
	public void removeObject (IWorldObject obj)
	{
		//remove object and its image
		objects.remove(obj);
		pane.getChildren().remove(obj.getImg());
	}
	
	public void testActivatable (IWorldObject mover, Point2D pos)
	{
		//see if this spot contains an activatable object
		for(IWorldObject obj : objects)
			if (obj.intersects(pos) && obj instanceof Activatable)
			{
				((Activatable) obj).onActivate(mover);
				return;
			}
	}
	
	public boolean canMoveTo(Point2D pos)
	{
		//check for islands
		if (!isValidPos(pos))
			return false;
		
		//check to see if there is an object in the way thats not an activatable (we can pass thru those)
		for(IWorldObject obj : objects)
			if (obj.intersects(pos) && !(obj instanceof Activatable))
				return false;
		
		return true;
	}
	
	public boolean isSpotOpen (Point2D pos)
	{
		//check for islands
		if (!isValidPos(pos))
			return false;
		
		//check for objects
		for(IWorldObject obj : objects)
			if (obj.intersects(pos))
				return false;
		
		return true;
	}
	
	public boolean isValidPos (Point2D pos)
	{
		//make sure it is within the map bounds
		if (pos.getX() > (map[0].length-1) || pos.getX() < 0 || pos.getY() > (map.length-1) || pos.getY() < 0)
			return false;
		
		//see if this is spot is not water
		if (map[(int) pos.getY()][(int) pos.getX()] != 0)
			return false;
		
		return true;
	}
	
	public void testPlayerCollision (Point2D pos)
	{
		//see if the player is in the specified position and game over if they are
		for (IWorldObject obj : objects)
			if (obj.intersects(pos) && obj instanceof Player)
				((Player)obj).gameOver();
	}
	
	public void snapCameraTo (Point2D pos)
	{
		//move camera so player is in the center of the screen
		pane.setLayoutX((cellCount-pos.getX() - ((cellCount/2)+1))*cellSize);
		pane.setLayoutY((cellCount-pos.getY() - ((cellCount/2)+1))*cellSize);
		
		//if the camera can see out of bounds, if so, move it inside of the map bounds
		if (pane.getLayoutY() < cameraYMax)
			pane.setLayoutY(cameraYMax);
		else if (pane.getLayoutY() > 0)
			pane.setLayoutY(0);
		
		if (pane.getLayoutX() < cameraXMax)
			pane.setLayoutX(cameraXMax);
		else if (pane.getLayoutX() > 0)
			pane.setLayoutX(0);
	}
	
	public Point2D findRandomOpenSpot ()
	{
		Point2D pos;
		do
		{
			pos = new Point2D(r.nextInt(map[0].length), r.nextInt(map.length));
		} while (!isSpotOpen(pos));
		
		return pos;
	}
